def sports_activity_progress_bar(goal, today_count):
    """Прогресс-бар по цели упражнений на день."""
    pct = min(100, round(today_count / goal * 100)) if goal else 0
    filled = int(pct / 10)
    empty = 10 - filled
    bar = "█" * filled + "░" * empty
    return f"📊 *Прогресс:* {today_count}/{goal} {bar} {pct}%\n\n"


