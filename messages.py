def plural_days(count):
    """Возвращает правильное склонение слова "день" для числа count."""

    # Особые случаи для чисел 11-19
    if 11 <= count % 100 <= 19:
        return f"{count} дней"

    # Для остальных смотрим последнюю цифру
    last_digit = count % 10

    if last_digit == 1:
        return f"{count} день"
    elif 2 <= last_digit <= 4:
        return f"{count} дня"
    else:
        return f"{count} дней"

